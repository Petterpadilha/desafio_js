/* 
Faça uma função chamada smallestNumber que recebe dois parâmetros, chamados val1 e val2 do tipo number 
e retorna um number, que é o parâmetro de menor valor.
*/

function smallestNumber(val1, val2) {
    return Math.min(val1, val2);
}
