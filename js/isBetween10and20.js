/*
Faça uma função chamada isBetween10and20 que recebe um parâmetro, chamado val do tipo number e retorna um 
boolean, indicando se o número é maior que 10 e menor que 20.
*/
function isBetween10and20(val) {
    return val > 10 && val < 20;
}
