/*
Faça uma função chamada increaseNumberBy10Pct, que recebe um parâmetro
chamado val do tipo number, e retornaum number equivalente ao valor do parâmetro + 10%.
 */

function increaseNumberBy10Pct(val) {
    return val + parseInt(val * 0.1);
}
