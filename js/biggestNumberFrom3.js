/*
Faça uma função chamada biggestNumberFrom3 que recebe três parâmetros chamados, val1, val2 e val3 do tipo number,
e retorna um number que é o parâmetro de maior valor.
*/

function biggestNumberFrom3(val1, val2, val3) {
    return Math.max(val1, val2, val3);
}
