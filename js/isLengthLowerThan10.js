/*
Faça uma função chamada isLengthLowerThan10 que recebe um parâmetro, chamado val do tipo string
 e retorna um boolean  indicando, se o comprimento da string é menor que 10.
*/

function isLengthLowerThan10(val) {
    return val.length < 10;
}
