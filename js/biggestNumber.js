/*
Faça uma função chamada biggestNumber que receb dois parâmetros, chamados val1 e val2 do tipo 
number, e retorna um number que é o parâmetro de maior valor.
*/

function biggestNumber(val1, val2) {
    return Math.max(val1, val2);
}

